// Подключение стандартных библиотек для ввода/вывода и работы со строками
#include <iostream>
#include <string>
// Главная функция программы
int main()
{
	// Установка русской локали для корректного отображения кириллицы
	setlocale(LC_ALL, "RU");
	// Объявление константы для размера массива городов
	const int size = 10;
	// Создание массива строк для хранения названий городов
	std::string cities[size];
	// Вывод приглашения для ввода названий 10 городов
	std::cout << "Введите названия 10 городов:" << std::endl;
	// Цикл для ввода названий городов
	for (int i = 0; i < size; ++i)
	{
		// Вывод номера города для ввода
		std::cout << "Город " << i + 1 << ": ";
		// Чтение всей строки с названием города и сохранение в массив
		getline(std::cin, cities[i]);
	}
	// Объявление переменной для подсчета городов без буквы 'р'
	int count = 0;
	// Цикл для перебора всех городов в массиве
	for (const auto& city : cities)
	{
		// Флаг для проверки наличия буквы 'р' в названии города
		bool has_p = false;
		// Цикл для перебора каждого символа в названии города
		for (char ch : city)
		{
			// Проверка, является ли символ буквой 'р' (регистр не важен)
			if (ch == 'p' || ch == 'P')
			{
				// Если буква найдена, устанавливаем флаг в true
				has_p = true;
				// Прерываем внутренний цикл, так как буква уже найдена
				break;
			}
		}
		// Если буква 'р' не найдена в названии города, увеличиваем счетчик
		if (!has_p)
		{
			count++;
		}
	}
	// Вывод результата: количество городов без буквы 'р'
	std::cout << "\nКоличество городов без буквы 'р': " << count << std::endl;
}