// Подключение стандартных библиотек для ввода/вывода и работы со строками
#include <iostream>
#include <string>
// Главная функция программы
int main()
{
	// Установка русской локали для корректного отображения кириллицы
	setlocale(LC_ALL, "RU");
	// Задание константы для размера массива (20 элементов)
	const int size = 20;
	// Создание массива строк для хранения названий горных вершин
	std::string mountains[size];
	// Вывод сообщения с просьбой ввести названия 20 горных вершин
	std::cout << "Введите названия 20 горных вершин:" << std::endl;
	// Цикл для ввода названий 20 горных вершин
	for (int i = 0; i < size; ++i)
	{
		// Вывод номера вершины для ввода
		std::cout << "Вершина " << i + 1 << ": ";
		// Считывание названия вершины с клавиатуры
		getline(std::cin, mountains[i]);
	}
	// Инициализация переменной st первым элементом массива
	std::string st = mountains[0];
	// Цикл для поиска самого короткого названия
	for (const auto& mountain : mountains)
	{
		// Если текущее название короче, чем st
		if (mountain.length() < st.length())
		{
			// Обновляем st на текущее название
			st = mountain;
		}
	}
	// Вывод результата: самое короткое название горной вершины
	std::cout << "\nСамое короткое название горной вершины: " << st << std::endl;
}