// Подключение стандартных библиотек для ввода/вывода, работы с векторами, алгоритмами и строками
#include <iostream>
#include <vector>
#include <algorithm>
#include <string>
// Главная функция программы
int main()
{
	// Установка русской локали для корректного отображения кириллицы
	setlocale(LC_ALL, "RU");
	// Объявление константы для размера массива городов
	const int size = 10;
	// Объявление массива для хранения названий городов
	std::string cities[size];
	// Вывод приглашения для ввода названий 10 городов
	std::cout << "Введите названия 10 городов:" << std::endl;
	// Цикл для ввода названий городов
	for (int i = 0; i < size; ++i)
	{
		// Вывод номера города для ввода
		std::cout << "Город " << i + 1 << ": ";
		// Чтение всей строки с названием города из консоли
		getline(std::cin, cities[i]);
	}
	// Объявление вектора для хранения городов, оканчивающихся на 'v'
	std::vector<std::string> filtered_cities;
	// Цикл для фильтрации городов, оканчивающихся на 'v'
	for (const auto& city : cities)
	{
		// Проверка, что строка не пустая и оканчивается на 'v'
		if (!city.empty() && city.back() == 'v')
		{
			// Добавление города в вектор filtered_cities
			filtered_cities.push_back(city);
		}
	}
	// Сортировка отфильтрованных городов по алфавиту
	sort(filtered_cities.begin(), filtered_cities.end());
	// Вывод заголовка для списка городов, оканчивающихся на 'v'
	std::cout << "\nГорода, оканчивающиеся на 'v', отсортированные по алфавиту:" << std::endl;
	// Цикл для вывода отсортированных городов
	for (const auto& city : filtered_cities)
	{
		// Вывод названия города
		std::cout << city << std::endl;
	}
}