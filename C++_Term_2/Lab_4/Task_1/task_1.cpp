// Подключение стандартных библиотек для ввода/вывода и работы со строками
#include <iostream>
#include <string>
// Главная функция программы
int main()
{
	// Установка русской локали для корректного отображения кириллицы
	setlocale(LC_ALL, "RU");
	// Константа, определяющая количество городов
	const int size = 10;
	// Массив строк для хранения названий городов
	std::string cities[size];
	// Вывод приглашения для ввода названий 10 городов
	std::cout << "Введите названия 10 городов:" << std::endl;
	// Цикл для ввода названий городов
	for (int i = 0; i < size; ++i)
	{
		// Вывод номера текущего города
		std::cout << "Город " << i + 1 << ": ";
		// Считывание названия города с клавиатуры
		getline(std::cin, cities[i]);
	}
	// Вывод заголовка для результатов
	std::cout << "\nПорядковые номера городов с названием из 7 букв:" << std::endl;
	// Цикл для поиска городов с названием из 7 букв
	for (int i = 0; i < size; ++i)
	{
		// Проверка, состоит ли название города из 7 букв
		if (cities[i].length() == 7)
		{
			// Вывод информации о городе, если условие выполнено
			std::cout << "Город \"" << cities[i] << "\" имеет порядковый номер: " << i + 1 << std::endl;
		}
	}
}