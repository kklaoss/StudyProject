// Подключение стандартной библиотеки для ввода/вывода
#include <iostream>
// Подключение стандартной библиотеки для работы с векторами
#include <vector>
// Подключение заголовочного файла с объявлением класса Num
#include "task_1.h"
// Главная функция программы
int main()
{
	// Установка русской локали для корректного отображения кириллицы
	setlocale(LC_ALL, "RU");
	// Переменная для хранения количества пар чисел
	int count;
	// Вектор для хранения объектов класса Num
	std::vector<Num> pairs;
	// Запрос количества пар чисел у пользователя
	std::cout << "Введите количество пар чисел: ";
	std::cin >> count;
	// Проверка корректности введенного количества
	if (count <= 0)
	{
		// Сообщение об ошибке, если количество не положительное
		std::cout << "Некорректное количество пар!\n";
		// Возврат кода ошибки
		return 1;
	}
	// Запрос на ввод информации об объектах
	std::cout << "Заполните информацию об объектах:\n";
	// Цикл для ввода count пар чисел
	for (int i = 0; i < count; ++i)
	{
		// Переменные для временного хранения вводимых значений
		int x, y;
		// Вывод номера текущего объекта
		std::cout << "\nОбъект #" << i + 1 << std::endl;
		// Запрос значения x
		std::cout << "Введите x: ";
		std::cin >> x;
		// Запрос значения y
		std::cout << "Введите y: ";
		std::cin >> y;
		// Добавление нового объекта Num в вектор (с использованием emplace_back для эффективности)
		pairs.emplace_back(x, y);
	}
	// Вывод заголовка для раздела с результатами
	std::cout << "\nРезультаты:\n";
	// Цикл для вывода информации обо всех объектах
	for (size_t i = 0; i < pairs.size(); ++i)
	{
		// Вывод разделителя и номера объекта
		std::cout << "\n--------------------------------\nОбъект #" << i + 1
			<< "\n--------------------------------\n" << std::endl;
		// Вывод значений x и y текущего объекта
		pairs[i].print();
		// Вычисление и вывод квадрата меньшего числа
		pairs[i].math();
	}
}