// Подключение библиотеки для работы со строками
#include <string>
// Объявление класса Worker (Работник)
class Worker
{
private:
	// Фамилия работника (строка)
	std::string last_name;
	// Зарплата работника (вещественное число)
	double salary;
	// Год рождения работника (целое число)
	int year_on_birth;
public:
	// Конструктор по умолчанию (без параметров)
	Worker();
	// Конструктор с параметрами (фамилия, зарплата, год рождения)
	Worker(std::string worker_name, double worker_salary, int worker_year_on_birth);
	// Деструктор (вызывается при уничтожении объекта)
	~Worker();
	// Метод для вычисления и вывода возраста работника (const - не изменяет объект)
	void age() const;
	// Метод для вычисления дней до 50-летия работника (const - не изменяет объект)
	void days_until_50() const;
	// Метод для вывода информации о работнике (const - не изменяет объект)
	void print() const;
};