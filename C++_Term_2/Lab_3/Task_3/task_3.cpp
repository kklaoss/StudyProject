// Подключение стандартных библиотек для ввода/вывода, работы со строками и алгоритмами
#include <iostream>
#include <algorithm>
#include <string>
// Главная функция программы
int main()
{
	// Установка русской локали для корректного отображения кириллицы
	setlocale(LC_ALL, "RU");
	// Объявление переменной для хранения введенного текста
	std::string text;
	// Вывод приглашения для ввода текста
	std::cout << "Введите строку текста: ";
	// Чтение всей строки текста из консоли
	std::getline(std::cin, text);
	// Проверка: если длина строки меньше 7 символов, выводим сообщение об ошибке и завершаем программу
	if (text.length() < 7)
	{
		std::cout << "Строка слишком короткая для сортировки последних 7 символов." << std::endl;
		return 1; // Завершение программы с кодом ошибки 1
	}
	// Извлечение последних 7 символов из введенной строки
	std::string last_seven = text.substr(text.length() - 7);
	// Сортировка последних 7 символов по возрастанию
	std::sort(last_seven.begin(), last_seven.end());
	// Замена последних 7 символов в исходной строке на отсортированные
	text.replace(text.length() - 7, 7, last_seven);
	// Переход на новую строку для красоты вывода
	std::cout << std::endl;
	// Вывод результата с измененной строкой
	std::cout << "Результат: " << text << std::endl;
}