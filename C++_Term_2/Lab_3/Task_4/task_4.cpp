// Подключение стандартных библиотек для ввода/вывода и работы со строками
#include <iostream>
#include <string>
// Главная функция программы
int main()
{
	// Установка русской локали для корректного отображения кириллицы
	setlocale(LC_ALL, "RU");
	// Объявление переменной для хранения первой строки
	std::string str_1;
	// Вывод приглашения для ввода первой строки
	std::cout << "Введите первую строку: ";
	// Чтение всей строки из консоли и сохранение в переменную str_1
	std::getline(std::cin, str_1);
	// Объявление переменной для хранения второй строки
	std::string str_2;
	// Вывод приглашения для ввода второй строки
	std::cout << "Введите вторую строку: ";
	// Чтение всей строки из консоли и сохранение в переменную str_2
	std::getline(std::cin, str_2);
	// Объявление переменной-флага для проверки, все ли символы из первой строки найдены во второй
	bool all_chars_found = true;
	// Перебор каждого символа первой строки
	for (auto ch_1 : str_1)
	{
		// Объявление переменной-флага для проверки, найден ли текущий символ во второй строке
		bool found = false;
		// Перебор каждого символа второй строки
		for (auto ch_2 : str_2)
		{
			// Если символ из первой строки найден во второй строке
			if (ch_1 == ch_2)
			{
				// Установка флага в true
				found = true;
				// Прерывание внутреннего цикла
				break;
			}
		}
		// Если символ из первой строки не найден во второй строке
		if (!found)
		{
			// Установка флага all_chars_found в false
			all_chars_found = false;
			// Прерывание внешнего цикла
			break;
		}
	}
	// Проверка, все ли символы из первой строки найдены во второй строке
	if (all_chars_found)
	{
		// Переход на новую строку для красоты вывода
		std::cout << std::endl;
		// Вывод сообщения о том, что все символы найдены
		std::cout << "Все символы из первой строки встречаются во второй строке." << std::endl;
	}
	else
	{
		// Переход на новую строку для красоты вывода
		std::cout << std::endl;
		// Вывод сообщения о том, что не все символы найдены
		std::cout << "Не все символы из первой строки встречаются во второй строке." << std::endl;
	}
	// Поиск первого вхождения подстроки "computer" в первой строке
	size_t pos_1 = str_1.find("computer");
	// Пока вхождения подстроки "computer" найдены
	while (pos_1 != std::string::npos)
	{
		// Замена подстроки "computer" на "PC" в первой строке
		str_1.replace(pos_1, 8, "PC");
		// Поиск следующего вхождения подстроки "computer" после текущей позиции
		pos_1 = str_1.find("computer", pos_1 + 2);
	}
	// Поиск первого вхождения подстроки "computer" во второй строке
	size_t pos_2 = str_2.find("computer");
	// Пока вхождения подстроки "computer" найдены
	while (pos_2 != std::string::npos)
	{
		// Замена подстроки "computer" на "PC" во второй строке
		str_2.replace(pos_2, 8, "PC");
		// Поиск следующего вхождения подстроки "computer" после текущей позиции
		pos_2 = str_2.find("computer", pos_2 + 2);
	}
	// Вывод первой строки после замены
	std::cout << "Первая строка после замены: " << str_1 << std::endl;
	// Вывод второй строки после замены
	std::cout << "Вторая строка после замены: " << str_2 << std::endl;
}