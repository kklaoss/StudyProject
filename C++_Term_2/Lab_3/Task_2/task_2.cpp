// Подключение стандартных библиотек для ввода/вывода и работы со строками
#include <iostream>
#include <string>
// Главная функция программы
int main()
{
	// Установка русской локали для корректного отображения кириллицы
	setlocale(LC_ALL, "RU");
	// Вывод приглашения для ввода текста
	std::cout << "Введите текст: ";
	// Объявление переменной для хранения введенного текста
	std::string input;
	// Чтение всей строки текста из консоли
	getline(std::cin, input);
	// Объявление временной строки для хранения текста без символов '[' и ']'
	std::string temp;
	// Цикл для прохода по каждому символу введенного текста
	for (auto c : input)
	{
		// Проверка: если символ не является '[' или ']', добавляем его во временную строку
		if (c != '[' && c != ']')
		{
			temp += c;
		}
	}
	// Объявление строки для хранения итогового результата
	std::string result;
	// Счетчик предложений в тексте
	int sentence_count = 0;
	// Цикл для обработки временной строки и формирования итогового текста
	for (int i = 0; i < temp.size(); ++i)
	{
		// Добавление текущего символа в итоговую строку
		result += temp[i];
		// Проверка: если текущий символ - точка, увеличиваем счетчик предложений
		if (temp[i] == '.')
		{
			sentence_count++;
			// Проверка: если после точки нет пробела, добавляем пробел
			if (i + 1 < temp.size() && temp[i + 1] != ' ')
			{
				result += ' ';
			}
		}
	}
	// Переход на новую строку для красоты вывода
	std::cout << std::endl;
	// Вывод исправленного текста
	std::cout << "Исправленный текст: " << result << std::endl;
	// Вывод количества предложений в тексте
	std::cout << "Количество предложений: " << sentence_count << std::endl;
}