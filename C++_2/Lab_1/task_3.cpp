#include <iostream>  // Подключение библиотеки для работы с вводом и выводом
#include <fstream>   // Подключение библиотеки для работы с файлами
#include <vector>    // Подключение библиотеки для использования контейнера vector
int main()
{
    setlocale(LC_ALL, "RU");  // Установка локали для поддержки кириллицы в консоли
    std::ofstream out_file("file.txt");  // Создание объекта для записи в файл "file.txt"
    out_file << "1 32 5 54 2 31 24 8 38 13 42" << std::endl;  // Запись строки с числами в файл
    out_file.close();  // Закрытие файла после записи
    std::ifstream in_file("file.txt");  // Создание объекта для чтения из файла "file.txt"
    int number;  // Переменная для хранения текущего числа из файла
    std::vector<int> numbers;  // Вектор для хранения всех чисел из файла
    while (in_file >> number)  // Чтение чисел из файла до конца файла
    {
        numbers.push_back(number);  // Добавление прочитанного числа в вектор
    }
    in_file.close();  // Закрытие файла после чтения
    for (int& num : numbers)  // Проход по всем числам в векторе
    {
        if (num >= -10 && num <= 10)  // Проверка, находится ли число в диапазоне [-10, 10]
        {
            num = -num;  // Изменение знака числа на противоположный
        }
    }
    std::ofstream new_file("new_file.txt");  // Создание объекта для записи в новый файл "new_file.txt"
    for (int num : numbers)  // Проход по всем числам в векторе
    {
        new_file << num << " ";  // Запись числа в новый файл с пробелом после каждого числа
    }
    new_file.close();  // Закрытие нового файла после записи
    std::cout << "Числа успешно изменены и записаны в файл new_file.txt";  // Вывод сообщения об успешном завершении
}